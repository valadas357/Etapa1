'''
1° Escreva um algoritmo para determinar se um número A é divisível por um outro número
B. Esses valores devem ser fornecidos pelo usuário.
'''

num1 = float(input('Digite um numero: '))
num2 = float(input('Digite um segundo numero: '))

div = num1 / num2

print(f'O primeiro numero é divisivel pelo segundo, cujo o resultado é: {div}')
