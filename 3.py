# solicitando 3 notas ao usuario para ter a media
nota1 = float(input('Digite a primeira nota:'))
nota2 = float(input('Digite a segunda nota:'))
nota3= float(input('Digite a terceira nota:'))

#Fazendo a soma das 3 notas e dividindo por 3 para ter a media
media= (nota1+nota2+nota3)/3

#Imprimindo os valores para fazer a media aritimetica das 3 notas
print(f'A media aritimetica das 3 notas Ã©: {media:.2f} ')